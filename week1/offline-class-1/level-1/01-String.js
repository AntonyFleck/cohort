let str="Hello  World"

// Length
console.log("Length:", str.length);

// indexOf
console.log("Index:", str.indexOf("World"));

// lastIndexOf
console.log("Index:", str.lastIndexOf("o"));

// slice
console.log("After slice:", str.slice(1,6));

// substring
console.log("After substring:", str.substring(1,6));

// replace
console.log("After replace:", str.replace("World","puneet"));

// split
console.log("After split:", str.split("l"));

// trim takes  of initial extra spaces 
console.log("After trim:", str.trim());


// toUpperCase
console.log("After toUpperCase:", str.toUpperCase());


// toLowerCase
console.log("After toLowerCase:", str.toLowerCase());

